# Step 1: Read two integers a and b
a = int(input("Enter the first positive integer (a): "))
b = int(input("Enter the second positive integer (b): "))

# Step 2: Calculate the square of the hypotenuse
c_squared = a**2 + b**2  # This is c^2 = a^2 + b^2

# Step 3: Print the result
print("The square of the hypotenuse is:", c_squared)
